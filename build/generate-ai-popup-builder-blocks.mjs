import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { globby } from "globby";

const buildDir = dirname( fileURLToPath( import.meta.url ) );
const rootDir = dirname( buildDir );
const outputs = [
    {
        label: "free",
        sourceRoot: "src",
        outputPath: join( rootDir, "includes/AI/PopupBuilder/Blueprint/generated-fooconvert-blocks.php" ),
    },
    {
        label: "PRO",
        sourceRoot: "pro/src",
        outputPath: join( rootDir, "pro/includes/AI/PopupBuilder/Blueprint/generated-fooconvert-pro-blocks.php" ),
    },
];

for ( const output of outputs ) {
    const sortedMetadata = await collectBlockMetadata( output.sourceRoot );
    const contents = `<?php
/**
 * Generated FooConvert ${ output.label } block metadata for the AI popup builder.
 *
 * Do not edit this file directly. Run \`npm run build:blocks\` instead.
 */

defined( 'ABSPATH' ) || exit;

return ${ phpExport( sortedMetadata ) };
`;

    await mkdir( dirname( output.outputPath ), { recursive: true } );
    await writeFile( output.outputPath, contents, "utf8" );

    console.info( `Generated ${ Object.keys( sortedMetadata ).length } FooConvert ${ output.label } block metadata entr${ Object.keys( sortedMetadata ).length === 1 ? "y" : "ies" } at ${ output.outputPath }.` );
}

async function collectBlockMetadata( sourceRoot ) {
    const blockMetadata = {};

    const files = await globby( "**/block.json", {
        cwd: join( rootDir, sourceRoot ),
        onlyFiles: true,
    } );

    for ( const file of files ) {
        const filepath = join( rootDir, sourceRoot, file );
        const metadata = JSON.parse( await readFile( filepath, "utf8" ) );
        const blockName = typeof metadata?.name === "string" ? metadata.name : "";

        if ( !blockName.startsWith( "fc/" ) ) {
            continue;
        }

        blockMetadata[ blockName ] = normalizeMetadata( metadata );
    }

    return Object.fromEntries(
        Object.entries( blockMetadata ).sort( ( [ left ], [ right ] ) => left.localeCompare( right ) )
    );
}

function normalizeMetadata( metadata ) {
    return pickDefined( {
        name: metadata.name,
        title: metadata.title,
        description: metadata.description,
        category: metadata.category,
        parent: normalizeStringList( metadata.parent ),
        ancestor: normalizeStringList( metadata.ancestor ),
        attributes: isPlainObject( metadata.attributes ) ? metadata.attributes : {},
        supports: isPlainObject( metadata.supports ) ? metadata.supports : {},
        inserter: typeof metadata.inserter === "boolean" ? metadata.inserter : undefined,
    } );
}

function normalizeStringList( value ) {
    if ( typeof value === "string" && value.length > 0 ) {
        return [ value ];
    }

    if ( !Array.isArray( value ) ) {
        return [];
    }

    return value.filter( item => typeof item === "string" && item.length > 0 );
}

function pickDefined( value ) {
    return Object.fromEntries(
        Object.entries( value ).filter( ( [ , item ] ) => item !== undefined )
    );
}

function isPlainObject( value ) {
    return Boolean( value ) && Object.prototype.toString.call( value ) === "[object Object]";
}

function phpExport( value, indent = 0 ) {
    const padding = "    ".repeat( indent );
    const nextPadding = "    ".repeat( indent + 1 );

    if ( value === null ) {
        return "null";
    }

    if ( typeof value === "boolean" ) {
        return value ? "true" : "false";
    }

    if ( typeof value === "number" ) {
        return Number.isFinite( value ) ? String( value ) : "0";
    }

    if ( typeof value === "string" ) {
        return `'${ value.replace( /\\/g, "\\\\" ).replace( /'/g, "\\'" ) }'`;
    }

    if ( Array.isArray( value ) ) {
        if ( value.length === 0 ) {
            return "array()";
        }

        return `array(
${ value.map( item => `${ nextPadding }${ phpExport( item, indent + 1 ) },` ).join( "\n" ) }
${ padding })`;
    }

    if ( isPlainObject( value ) ) {
        const entries = Object.entries( value );
        if ( entries.length === 0 ) {
            return "array()";
        }

        return `array(
${ entries.map( ( [ key, item ] ) => `${ nextPadding }${ phpExport( key ) } => ${ phpExport( item, indent + 1 ) },` ).join( "\n" ) }
${ padding })`;
    }

    return "null";
}
